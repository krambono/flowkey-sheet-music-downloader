  <h1 class="title">Allow CORS</h1>
  <hr />
  <div class="block">
    You need to allow <strong>CORS</strong> on your browser to be able to make AJAX requests to the Flowkey website.
    <br />
    You can use the following extension on chromium based browser (Chrome, Brave, Edge etc.):
    <a href="https://chrome.google.com/webstore/detail/allow-cors-access-control/lhobafahddgcelffkeicbaginigeejlf"
      >Allow CORS: Access-Control-Allow-Origin</a
    >.
    <br />
    <strong>Do not forget</strong> to disable it after you downloaded the sheet.
  </div>
</section>
